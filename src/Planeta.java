
public class Planeta {
	//Atributo de classe
	private String mercurio;
	private String venus;
	private String terra;
	private String marte;
	private String jupiter;
	private String saturno;
	private String urano;
	private String netuno;
	
	public Planeta() {} 
	
	//Construtor da classe
	public void Pessoa (String mercurio, String venus, String terra, String marte, String jupiter, String saturno, String urano, String netuno) {
		this.mercurio = mercurio;
		this.venus = venus;
		this.terra = terra;
		this.marte = marte;
		this.jupiter = jupiter;
		this.saturno = saturno;
		this.urano = urano;
		this.netuno = netuno;
	}

	public String getMercurio() {
		return mercurio;
	}

	public void setMercurio(String mercurio) {
		this.mercurio = mercurio;
	}

	public String getVenus() {
		return venus;
	}

	public void setVenus(String venus) {
		this.venus = venus;
	}

	public String getTerra() {
		return terra;
	}

	public void setTerra(String terra) {
		this.terra = terra;
	}

	public String getMarte() {
		return marte;
	}

	public void setMarte(String marte) {
		this.marte = marte;
	}

	public String getJupiter() {
		return jupiter;
	}

	public void setJupiter(String jupiter) {
		this.jupiter = jupiter;
	}

	public String getSaturno() {
		return saturno;
	}

	public void setSaturno(String saturno) {
		this.saturno = saturno;
	}

	public String getUrano() {
		return urano;
	}

	public void setUrano(String urano) {
		this.urano = urano;
	}

	public String getNetuno() {
		return netuno;
	}

	public void setNetuno(String netuno) {
		this.netuno = netuno;
	}
	
	

}
